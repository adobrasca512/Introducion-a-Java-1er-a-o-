package T3_Variables_Constantes;

public class Ejercicio_1 {

	public static void main(String[] args) {
	int valor1 = 8;
	int valor2 = 3;
	// Dadas dos variables enteras (inicializadas), calcular e imprimir por consola
    int suma=valor1+valor2;
    int resta=valor1-valor2;
    int producto=valor1*valor2;
    int media=valor1/valor2;
    System.out.println("El resulado de la suma es: " +suma);
    System.out.println("El resulado de la resta es: " +resta);
    System.out.println("El resulado del producto es: " +producto);
    System.out.println("El resulado de la media es: " +media);
	}

}
